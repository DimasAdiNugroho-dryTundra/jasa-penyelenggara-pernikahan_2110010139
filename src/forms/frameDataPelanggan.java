    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            if (txtIDPelanggan.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Id Pelanggan Belum Diisi");
                txtIDPelanggan.requestFocus();
            } else if
               (txtIDUser.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Id User Belum Diisi");
                txtIDUser.requestFocus();
            } else if
               (txtNamaPelanggan.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nama Pelanggan Belum Diisi");
                txtNamaPelanggan.requestFocus();
            } else if
               (txtJenisKelamin.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Jenis Kelamin Belum Diisi");
                txtJenisKelamin.requestFocus();
            } else if
               (txtNoTelpon.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "No Telpon Belum Diisi");
                txtNoTelpon.requestFocus();
            } else if
               (txtTanggalLahir.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Tanggal Lahir Belum Diisi");
                txtTanggalLahir.requestFocus();
            } else if
               (txtToken.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Token Belum Diisi");
                txtToken.requestFocus();
            } else if
               (txtAlamat.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Alamat Belum Diisi");
                txtAlamat.requestFocus();
            } else {
                String sqlCari = "SELECT * FROM tbl_datapelanggan WHERE id_pelanggan = ?";
                PreparedStatement cari = objekku.konekDB.prepareStatement(sqlCari);
                cari.setString(1, txtIDPelanggan.getText());
                ResultSet hasil = cari.executeQuery();
                
                if (hasil.next()) {
                JOptionPane.showMessageDialog(this, "ID Pelanggan Sudah Ada");
                txtIDPelanggan.setText(hasil.getString("id_pelanggan"));
                txtIDUser.setText(hasil.getString("id_user"));
                txtNamaPelanggan.setText(hasil.getString("nama_pelanggan"));
                txtJenisKelamin.setText(hasil.getString("jenis_kelamin"));
                txtNoTelpon.setText(hasil.getString("no_telpon"));
                txtTanggalLahir.setText(hasil.getString("tgl_lahir"));
                txtToken.setText(hasil.getString("token"));
                txtAlamat.setText(hasil.getString("alamat"));
                } else {
                
                String sqlSimpan = "INSERT INTO tbl_datapelanggan VALUES (?,?,?,?,?,?,?,?)";
                PreparedStatement kueri = objekku.konekDB.prepareStatement(sqlSimpan);
                
                kueri.setString(1, txtIDPelanggan.getText());
                kueri.setString(2, txtIDUser.getText());
                kueri.setString(3, txtNamaPelanggan.getText());
                kueri.setString(4, txtJenisKelamin.getText());
                kueri.setString(5, txtNoTelpon.getText());
                kueri.setString(6, txtTanggalLahir.getText());
                kueri.setString(7, txtToken.getText());
                kueri.setString(8, txtAlamat.getText());
                
                kueri.executeUpdate();
                loadTabelDataPelanggan();
                
                JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.toString());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

