    void loadTabelDataPelanggan(){
        try {
            String sql = "SELECT * FROM tbl_datapelanggan";
            PreparedStatement kueri = objekku.konekDB.prepareStatement(sql);
            ResultSet data = kueri.executeQuery();
            
            while (data.next()) {
                Object[] baris = new Object[8];
                baris[0] = data.getString("id_pelanggan");
                baris[1] = data.getString("id_user");
                baris[2] = data.getString("nama_pelanggan");
                baris[3] = data.getString("jenis_kelamin");
                baris[4] = data.getString("no_telpon");
                baris[5] = data.getString("tgl_lahir");
                baris[6] = data.getString("token");
                baris[7] = data.getString("alamat");
                
                modelTabelDataPelanggan.addRow(baris);
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

